<script setup>
import navItemsA from '@/navigation/vertical'
import navItemsV from '@/navigation/verticalVisor'
import navItemsWm from '@/navigation/verticalWebmaster'
import { useThemeConfig } from '@core/composable/useThemeConfig'

// Components
import Footer from '@/layouts/components/Footer.vue'
import NavbarThemeSwitcher from '@/layouts/components/NavbarThemeSwitcher.vue'
import UserProfile from '@/layouts/components/UserProfile.vue'

// @layouts plugin
import { VerticalNavLayout } from '@layouts'

const { appRouteTransition, isLessThanOverlayNavBreakpoint } = useThemeConfig()
const { width: windowWidth } = useWindowSize()

const resolveRole = computed(() =>{
let rol = localStorage.getItem('role');
if(rol== 'administrador'){
  return navItemsA;
}else if(rol== 'webmaster'){
  return navItemsWm;  
}else{
  return navItemsV;
}
});

</script>

<template>
  <VerticalNavLayout
    :nav-items="resolveRole"
  >
    <!-- ðŸ‘‰ navbar -->
    <template #navbar="{ toggleVerticalOverlayNavActive }">
      <div class="d-flex h-100 align-center">
        <VBtn
          v-if="isLessThanOverlayNavBreakpoint(windowWidth)"
          icon
          variant="text"
          color="default"
          class="ms-n3"
          size="small"
          @click="toggleVerticalOverlayNavActive(true)"
        >
          <VIcon
            icon="tabler-menu-2"
            size="24"
          />
        </VBtn>

        <NavbarThemeSwitcher />

        <VSpacer />

        <UserProfile />
      </div>
    </template>

    <!-- ðŸ‘‰ Pages -->
    <RouterView v-slot="{ Component }">
      <Transition
        :name="appRouteTransition"
        mode="out-in"
      >
        <Component :is="Component" />
      </Transition>
    </RouterView>

    <!-- ðŸ‘‰ Footer -->
    <template #footer>
      <Footer />
    </template>

    <!-- ðŸ‘‰ Customizer -->
    <!-- <TheCustomizer /> -->
  </VerticalNavLayout>
</template>
